package swapOperator;

public class ItemTabu {
	private int item1=0;
	private int item2=0;
	
	public ItemTabu(int item1, int item2) {
		this.item1=item1;
		this.item2=item2;
	}

	public int getItem1() {
		return item1;
	}

	public void setItem1(int item1) {
		this.item1 = item1;
	}

	public int getItem2() {
		return item2;
	}

	public void setItem2(int item2) {
		this.item2 = item2;
	}
	
	
}
